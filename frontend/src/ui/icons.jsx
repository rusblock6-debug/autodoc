// Единый набор иконок (stroke-based, наследуют currentColor).
// size управляет шириной/высотой, остальные пропсы пробрасываются в <svg>.
function Icon({ children, size = 16, fill = 'none', strokeWidth = 2, ...props }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={fill}
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      {...props}
    >
      {children}
    </svg>
  )
}

